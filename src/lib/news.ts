export function getNewsByYear(): Record<number, NewsItem[]> {
  const grouped: Record<number, NewsItem[]> = {};
  for (const item of newsItems) {
    if (!grouped[item.year]) {
      grouped[item.year] = [];
    }
    grouped[item.year].push(item);
  }
  return grouped;
}

export function getNewsBySlug(slug: string): NewsItem | undefined {
  return newsItems.find((item) => item.slug === slug);
}

export function getLatestNews(count: number = 5): NewsItem[] {
  return [...newsItems]
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, count);
}

export function getAdjacentNews(slug: string): { prev: NewsItem | null; next: NewsItem | null } {
  const index = newsItems.findIndex((item) => item.slug === slug);
  return {
    prev: index > 0 ? newsItems[index - 1] : null,
    next: index < newsItems.length - 1 ? newsItems[index + 1] : null,
  };
}
