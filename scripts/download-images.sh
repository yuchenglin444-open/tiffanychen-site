# Download all artwork images from tiffanychen.site CDN
CDN="https://cdn.myportfolio.com/ff5f2b2d-778b-40f6-83f9-424596a725bf"

download() {
  local dir="$1"
  local filename="$2"
  local uuid_suffix="$3"
  local ext="${4:-jpg}"

  local url="${CDN}/${uuid_suffix}"
  local dest="public/images/${dir}/${filename}.${ext}"

  if [ ! -f "$dest" ]; then
    curl -sL -o "$dest" "$url"
    echo "Downloaded: $dest"
  else
    echo "Exists: $dest"
  fi
}

